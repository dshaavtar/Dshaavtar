// ─── Language Detection from Text ────────────────────────────────────────────

/**
 * Detect language from actual typed text using Unicode code-point ranges.
 * Returns 'en' as default for Latin/unknown scripts.
 */
export function detectLanguageFromText(text: string): SupportedLang {
  if (!text || text.trim().length === 0) return "en";

  let devanagariCount = 0;
  let tamilCount = 0;
  let teluguCount = 0;
  let bengaliCount = 0;
  let gujaratiCount = 0;
  let kannadaCount = 0;
  let malayalamCount = 0;
  let gurmukhiCount = 0;
  let arabicCount = 0;

  for (const ch of text) {
    const cp = ch.codePointAt(0) ?? 0;
    // Devanagari: U+0900–U+097F (Hindi, Marathi shared range)
    if (cp >= 0x0900 && cp <= 0x097f) devanagariCount++;
    // Tamil: U+0B80–U+0BFF
    else if (cp >= 0x0b80 && cp <= 0x0bff) tamilCount++;
    // Telugu: U+0C00–U+0C7F
    else if (cp >= 0x0c00 && cp <= 0x0c7f) teluguCount++;
    // Bengali: U+0980–U+09FF
    else if (cp >= 0x0980 && cp <= 0x09ff) bengaliCount++;
    // Gujarati: U+0A80–U+0AFF
    else if (cp >= 0x0a80 && cp <= 0x0aff) gujaratiCount++;
    // Kannada: U+0C80–U+0CFF
    else if (cp >= 0x0c80 && cp <= 0x0cff) kannadaCount++;
    // Malayalam: U+0D00–U+0D7F
    else if (cp >= 0x0d00 && cp <= 0x0d7f) malayalamCount++;
    // Gurmukhi (Punjabi): U+0A00–U+0A7F
    else if (cp >= 0x0a00 && cp <= 0x0a7f) gurmukhiCount++;
    // Arabic/Urdu: U+0600–U+06FF, U+0750–U+077F
    else if ((cp >= 0x0600 && cp <= 0x06ff) || (cp >= 0x0750 && cp <= 0x077f))
      arabicCount++;
  }

  const counts: [number, SupportedLang][] = [
    [devanagariCount, "hi"], // default to Hindi for Devanagari; Marathi same script
    [tamilCount, "ta"],
    [teluguCount, "te"],
    [bengaliCount, "bn"],
    [gujaratiCount, "gu"],
    [kannadaCount, "kn"],
    [malayalamCount, "ml"],
    [gurmukhiCount, "pa"],
    [arabicCount, "ur"],
  ];

  const best = counts.reduce(
    (max, cur) => (cur[0] > max[0] ? cur : max),
    [0, "en" as SupportedLang],
  );
  return best[0] > 0 ? best[1] : "en";
}

// ─── Session Language ─────────────────────────────────────────────────────────

/** Module-level session language — set once per session, never changed */
let _sessionLanguage: SupportedLang = "en";
let _sessionLanguageSet = false;

export function getSessionLanguage(): SupportedLang {
  return _sessionLanguage;
}

/**
 * Set session language from detected text. Only the FIRST call takes effect.
 * If the detected language is English, the next non-English detection can still
 * override it (allows "hi" on second message if first was "hi").
 */
export function setSessionLanguage(lang: SupportedLang): void {
  if (_sessionLanguageSet && _sessionLanguage !== "en") return;
  _sessionLanguage = lang;
  if (lang !== "en") _sessionLanguageSet = true;
}

export function clearSessionLanguage(): void {
  _sessionLanguage = "en";
  _sessionLanguageSet = false;
}

// ─── Translation Helpers ──────────────────────────────────────────────────────

/** Detect language from browser and map to SupportedLang (kept for legacy use) */
export function detectLanguage(): SupportedLang {
  const nav =
    typeof navigator !== "undefined" ? navigator.language || "" : "en";
  const code = nav.split("-")[0].toLowerCase();
  const supported: SupportedLang[] = [
    "en",
    "hi",
    "mr",
    "ta",
    "te",
    "bn",
    "gu",
    "kn",
    "ml",
    "pa",
    "ur",
  ];
  return supported.includes(code as SupportedLang)
    ? (code as SupportedLang)
    : "en";
}

/** Get translation for a key, falls back to English */
export function t(key: string, lang: SupportedLang): string {
  return TRANSLATIONS[key]?.[lang] ?? TRANSLATIONS[key]?.en ?? key;
}

/** Bilingual prefix: if lang is not English, prepend native translation */
export function bilingualPrefix(key: string, lang: SupportedLang): string {
  if (lang === "en") return "";
  const native = t(key, lang);
  return `${native}\n`;
}

/**
 * Return a bilingual string for a translation key.
 * When lang !== 'en': "<native text> | <english text>"
 * When lang === 'en': "<english text>" only
 */
export function bilingualMessage(key: string, lang: SupportedLang): string {
  const en = t(key, "en");
  if (lang === "en") return en;
  const native = t(key, lang);
  if (native === en) return en; // no translation available, show English only
  return `${native} | ${en}`;
}

/**
 * Return a bilingual raw string (not from TRANSLATIONS).
 * When lang !== 'en' and native != english: "<native> | <english>"
 * Falls back to english if no native provided.
 */
export function bilingualRaw(
  english: string,
  native: string | null | undefined,
  lang: SupportedLang,
): string {
  if (lang === "en" || !native || native === english) return english;
  return `${native} | ${english}`;
}
